char	ft_putchar(int x, int y, int i, int j)
{
	if ((i == 0) && (j == 0))
		return ('/');
	else if ((x - i == 1) && (j == 0))
		return ('\\');
	else if ((i == 0) && (y - j == 1))
		return ('\\');
	else if ((x - i == 1) && (y - j == 1))
		return ('/');
	else if (((i == 0) || (x - i == 1)) || ((j == 0) || (y - j == 1)))
		return ('*');
	else
		return (' ');
}
